import math

def bondpoints(current_level):
	if current_level < 1:
		raise Exception("Current bond level must be greater than or equal to 1.")
	
	tiers = [
		(1,   4,        40),
		(5,   18,       50),
		(19,  57,       60),
		(58,  151,      70),
		(152, math.inf, 150),
	]
	
	points = 60
	for level_start, level_end, points_diff in tiers:
		if current_level > level_start:
			level_diff = min(level_end - level_start + 1, current_level - level_start)
			points += level_diff * points_diff
		if level_end == math.inf or level_start >= current_level:
			break
	
	return points

for level in range(145, 155):
	print("Level", level, "points", bondpoints(level))

